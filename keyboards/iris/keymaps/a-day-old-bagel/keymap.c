bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case MALTRAK:
      if (record->event.pressed) {
        set_single_persistent_default_layer(N_MALTRAK);
      }
      return false;
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(N_QWERTY);
      }
      return false;
    case LOWER:
      if (record->event.pressed) {
        layer_on(N_LOWER);
        update_tri_layer(N_LOWER, N_RAISE, N_ADJUST);
      } else {
        layer_off(N_LOWER);
        update_tri_layer(N_LOWER, N_RAISE, N_ADJUST);
      }
      return false;
    case RAISE:
      if (record->event.pressed) {
        layer_on(N_RAISE);
        update_tri_layer(N_LOWER, N_RAISE, N_ADJUST);
      } else {
        layer_off(N_RAISE);
        update_tri_layer(N_LOWER, N_RAISE, N_ADJUST);
      }
      return false;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(N_ADJUST);
      } else {
        layer_off(N_ADJUST);
      }
      return false;
  }
  return true;
}
